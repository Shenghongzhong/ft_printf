#include "ft_printf.h"
#include "libft.h"
#include <stdlib.h>

int	ft_isflag(int c)
{
	return (ft_istype(c) || ft_isdigit(c) || ft_isspec(c));
}

int	ft_isspec(int c)
{
	if (c == '-' || c == '0' || c == '.' || c == '*' \
			|| c == '#' || c == ' ' || c == '+')
		return (1);
	return (0);
}

int	ft_istype(int c)
{
	if (c == 'c' || c == 's' || c == 'd' || c == 'i' || c == 'u' \
		|| c == 'x' || c == 'X' || c == 'p' || c == '%')
		return (1);
	return (0);	
}

int	ft_pad_width(int total_width, int size, int zero)
{
	int	count;

	count = 0;
	while (total_width - size > 0)
	{
		if (zero)
			count += ft_printchar('0');
		else
			count += ft_printchar(' ');
		total_width--;
	}
	return (count);
}
